package com.example.pwpbfinalproject.Transaksi;

public class TambahTransaksiInterface {
    final String petugas, bulan, tanggal, keterangan, siswa, bayar;

    public TambahTransaksiInterface(String petugas, String bulan, String tanggal, String keterangan, String siswa, String bayar) {
        this.petugas = petugas;
        this.bulan = bulan;
        this.tanggal = tanggal;
        this.keterangan = keterangan;
        this.siswa = siswa;
        this.bayar = bayar;
    }
}
